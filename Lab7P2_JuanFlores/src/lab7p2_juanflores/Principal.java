    private void btnabrirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnabrirMouseClicked
        
        if(selecciona.showDialog(null,"Abrir")==JFileChooser.APPROVE_OPTION){
            archivo=selecciona.getSelectedFile();
            if(archivo.canRead()){
                if(archivo.getName().endsWith("txt")){
                    String documento = abrirArchivo(archivo);
                    txtarea.setText(documento);
                    
                }else{
                    JOptionPane.showMessageDialog(this, "archivo no compatible");
                }
            }
        }
    }//GEN-LAST:event_btnabrirMouseClicked

    private void btnguardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnguardarMouseClicked
        
        if(selecciona.showDialog(null, "Guardar")==JFileChooser.APPROVE_OPTION){
            archivo=selecciona.getSelectedFile();
            if(archivo.getName().endsWith("txt")){
                String documento=txtarea.getText();
                String mensaje = guardarArchivo(archivo, documento);
                if(mensaje!=null){
                    JOptionPane.showMessageDialog(this, mensaje);
                }else{
                    JOptionPane.showMessageDialog(this, "archivo no compatible");
                }
            }else{
            JOptionPane.showMessageDialog(this, "guardar documento de texto");
        }
        }
    }//GEN-LAST:event_btnguardarMouseClicked


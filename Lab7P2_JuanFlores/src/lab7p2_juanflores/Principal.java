    private void cb_clientes(){
         DefaultComboBoxModel modelo = (DefaultComboBoxModel) cb_comprador.getModel();
        modelo.removeAllElements();
        
        
        
        
        File vendedoresAAA=null;
                
       
        
        try {
            
            vendedoresAAA= new File("./Clientes.txt");
            
            String vendedoresR = "";
            
            Scanner leer = new Scanner (vendedoresAAA);
            
            while(leer.hasNext()){
                
                vendedoresR += leer.next();
                
            }
            
            String [] vendedoArray = vendedoresR.split("]");
            
            String []token;
            
            for (int i = 0; i < vendedoArray.length; i++) {
                
                token = vendedoArray[i].split(",");
                
                for (int j = 0; j < token.length; j++) {
                    
                    if (j==0) {
                        String si = "";
                        for (int k = 1; k<token[j].length(); k++) {
                            
                            si+=token[j].charAt(k);
                        }
                        
                        modelo.addElement(si);
                       
                    }
                    
                }
                
            }
            
             leer.close();
            
        } catch (Exception e) {
            e.printStackTrace();
        }
         
        
        cb_comprador.setModel(modelo);
          
           
    }
